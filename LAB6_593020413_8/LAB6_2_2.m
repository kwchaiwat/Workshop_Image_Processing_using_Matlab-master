img = imread('img6_2.TIF');%??????????? img6_2.TIF ??????????????? img
img=im2double(img);%?????????????(img) ??????????????? double ??????????????? img
C = [1/9 1/9 1/9;1/9 1/9 1/9;1/9 1/9 1/9;];%?????????????3*3??????????????? 1/9
Conv = conv2(C,img);%??????????? convolution ??? 2D  ??????????????? Conv
[r,c] = size(img);%???????????????????????????? r ??? c
A=im2double(img);%?????????????(img) ??????????????? double ??????????????? A

for i=2:r-1
   for j=2:c-1
    C=(A(i-1,j-1)+A(i-1,j)+A(i-1,j+1)+A(i,j-1)+A(i,j+1)+A(i+1,j-1)+A(i+1,j)+A(i+1,j+1)+A(i,j))/9;
    A(i,j) = C;
   end
end


%???????????
subplot(1,3,1),imshow(img),title("Original");
subplot(1,3,2),imshow(Conv),title("Use Convolution");
subplot(1,3,3),imshow(A),title("Write function");


